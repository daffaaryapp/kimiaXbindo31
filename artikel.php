      <div class="row mt-5">
        <h3>Abstrak</h3>
        <p>
          Asam benzoat (C6H5COOH) merupakan bahan pengawet yang luas penggunaannya dan sering digunakan pada bahan makanan. Penggunaan bahan kimia sebagai bahan tambahan pada makanan saat ini sering ditemui pada makanan dan minuman. Salah
          satu bahan tambahan pada makanan adalah pengawet bahan kimia yang berfungsi untuk memperlambat kerusakan makanan, baik yang disebabkan mikroba pembusuk, bakteri, ragi maupun jamur dengan cara menghambat, mencegah, menghentikan
          proses pembusukan dan fermentasi dari bahan makanan.
        </p>
      </div>
      <div class="row mt-5">
        <h3>Pendahuluan</h3>
        <p>
          Saat ini, makanan dan minuman yang dihasilkan oleh industri makanan diolah sedemikian rupa sehingga makanan dan minuman tetap terjaga kualitasnya dalam kurun waktu tertentu, salah satunya yaitu dengan mengunakan asam benzoat
          sebagai bahan pengawet. Bahan pengawet adalah bahan tambahan pangan yang dapat mencegah atau menghambat proses fermentasi, pengasaman, atau pengurai lain terhadap makanan yang disebabkan oleh mikroorganisme. Penggunaan pengawet
          dalam pangan harus tepat, baik jenis maupun dosisnya. Suatu bahan pengawet mungkin efektif untuk mengawetkan pangan tertentu, tetapi tidak efektif untuk mengawetkan pangan lainnya karena pangan mempunyai sifat yang berbeda-beda
          sehingga mikroba perusak yang akan dihambat pertumbuhannya juga berbeda. Penggunaan bahan pengawet dari satu sisi menguntungkan karena dengan bahan pengawet, bahan pangan dapat dibebaskan dari kehidupan mikroba, baik yang bersifat
          patogen yang dapat menyebabkan keracunan atau gangguan kesehatan lainnya maupun mikrobial yang nonpatogen yang dapat menyebabkan kerusakan pangan, misalnya pembusukan. Namun dari sisi lain, bahan pengawet pada dasarnya adalah
          senyawa kimia yang merupakan bahan asing yang masuk bersamaan bahan pangan yang dikonsumsi. Apabila pemakaian bahan pangan dan dosisnya tidak diatur dan diawasi, kemungkinan besar akan menimbulkan Universitas Sumatera Utara
          kerugian bagi pemakaiannya, misalnya keracunan atau terakumulasinya pengawet dalam organ tubuh dan bersifat karsinogenik
        </p>
      </div>
      <div class="row mt-5">
        <h3>Kajian Teori</h3>
        <p>
          Mekanisme kerja asam benzoat atau garamnya berdasarkan pada permeabilitas membran sel mikroba terhadap molekul-molekul asam yang tidak terdisosiasi. Isi sel mikroba mempunyai pH yang selalu netral. Bila pH sitoplasma mikroba
          menjadi asam atau basa, maka akan terjadi gangguan pada organ-organ sel sehingga metabolisme terhambat dan akhirnya sel mati. Membran sel mikroba hanya permeabel terhadap molekul asam yang tidak terdisosiasi, maka untuk
          mendapatkan efektivitas yang tinggi sebaiknya asam-asam tersebut digunakan dalam lingkungan asam. Hal ini juga disebabkan pada pH netral dan basa, asam-asam organik terurai menjadi ion-ionnya (Winarno, F.G. dan B. S. Laksmi,
          1974).
          <br />
          Asam benzoat dan garamnya mempunyai aktivitas antimikroorganisme tergantung pada pH dan substrat, karena pH substrat sangat menentukan jumlah asam yang terdisosiasi. Pada pH 2,19 asam yang tidak terdisosiasi adalah 99%, pada pH
          4,2 asam yang tidak terdisosiasi adalah 50%. Natrium benzoat sebagai antimikroorganisme berperan dalam mengganggu permeabilitas membran sel. Asam benzoat mempunyai pH optimal untuk menghambat mikroorganisme yaitu pH 2,5-4,0. Asam
          benzoat dan natrium benzoat digunakan untuk menghambat pertumbuhan khamir (mikroorganisme uniseluler) dan bakteri tetapi kurang efektif untuk kapang (Afrianti, LH. 2010).
        </p>
      </div>
      <div class="row mt-5">
        <h3>Metode Penelitian</h3>
        <p>
          A.) Uji Ferri klorida. <br />
          Ekstrak asam benzoat dalam eter dari masing-masing sampel diuapkan sampai terbentuk residu sebanyak = 1 ml. Tambahkan beberapa tetes larutan 0,5% FeCl, kedalam residu. Terbentuknya endapan kuning muda dari ferri benzoat menandakan
          adanya asam benzoat. Kemudian, kedalamnya ditambahkan beberapa ml eter dan gojog. Endapan kuning muda tersebut akan larut kembali dalam eter, kemudian bandingkan dengan asam benzoat baku (AuterhoffKovar, 1987)
          <br />
          B.) Kromatografi Lapis Tipis. <br />
          Ekstrak asam benzoat dalam eter dari ketiga sampel dan larutan asam benzoat baku diuapkan pada penangas air sampai tertinggal I ml. Totolkan pada plat dengan fase diam silika gel GF 254 dan masukkan dalam bejana berisi fase gerak
          berupa heksan - asam asetat (96 : 4). Kembangkan dengan jarak perambatan 17 cm selama kurang lebih 30 menit. Biarkan plat silika gel GF 254 kering. Penampakan bercak dapat dengan sinar UV 254 nm. Hitung harga Rr dan bandingkan
          dengan harga Rr asam benzoat baku (Stahl, 1969).
          <br />
          C.) Profil Spektrum absorbansi. <br />
          Ekstrak asam benzoat dari masing-masing sampel dan asam benzoat baku dalam eter di scan dengan spektrofotometer uv/vis pada panjang gelombang 250 nm sampai 300 nm dengan interval 5 nm. Bandingkan profil spektrum absorbansi ekstrak
          asam benzoat pada sampel dengan profil spektrum absorbansi asam benzoat baku
        </p>
